    Route::post('/rules', [\App\Http\Controllers\AdminLoanRuleController::class, 'store'])->name('rules.store');
    Route::post('/rules/{id}/toggle', [\App\Http\Controllers\AdminLoanRuleController::class, 'toggleStatus'])->name('rules.toggle');
