    Route::post('/loans/{id}/reject', [\App\Http\Controllers\AdminLoanController::class, 'reject'])->name('loans.reject');
    
    // Admin Savings
    Route::get('/savings', [\App\Http\Controllers\AdminSavingsController::class, 'index'])->name('savings.index');
    Route::post('/savings/{id}/approve', [\App\Http\Controllers\AdminSavingsController::class, 'approve'])->name('savings.approve');
    Route::post('/savings/{id}/reject', [\App\Http\Controllers\AdminSavingsController::class, 'reject'])->name('savings.reject');
    
    // Admin Installments
    Route::get('/installments', [\App\Http\Controllers\AdminInstallmentController::class, 'index'])->name('installments.index');
    Route::post('/installments/{id}/approve', [\App\Http\Controllers\AdminInstallmentController::class, 'approve'])->name('installments.approve');
    Route::post('/installments/{id}/reject', [\App\Http\Controllers\AdminInstallmentController::class, 'reject'])->name('installments.reject');
    
    // Admin Reports
    Route::get('/reports', [\App\Http\Controllers\AdminReportController::class, 'index'])->name('reports.index');
    Route::get('/reports/loans/export', [\App\Http\Controllers\AdminReportController::class, 'exportLoans'])->name('reports.loans');
    Route::get('/reports/savings/export', [\App\Http\Controllers\AdminReportController::class, 'exportSavings'])->name('reports.savings');
    
