<?php

use App\Http\Controllers\ProfileController;
use App\Http\Controllers\MemberDashboardController;
use App\Http\Controllers\AdminDashboardController;
use App\Http\Controllers\LoanController;
use Illuminate\Support\Facades\Route;

Route::get('/', function () {
    return view('welcome');
});

// Global Authenticated Routes
Route::middleware('auth')->group(function () {
    Route::get('/notifications', [\App\Http\Controllers\NotificationController::class, 'index'])->name('notifications.index');
    Route::post('/notifications/read-all', [\App\Http\Controllers\NotificationController::class, 'markAllAsRead'])->name('notifications.readAll');
    Route::post('/notifications/{id}/read', [\App\Http\Controllers\NotificationController::class, 'markAsRead'])->name('notifications.read');
});

// Nasabah (Member) Routes
Route::middleware(['auth', 'role:member'])->group(function () {
    Route::get('/dashboard', [MemberDashboardController::class, 'index'])->name('dashboard');
    // Loans routes
    Route::get('/loans', [\App\Http\Controllers\MemberLoanController::class, 'index'])->name('loans.index');
    Route::get('/loans/create', [\App\Http\Controllers\MemberLoanController::class, 'create'])->name('loans.create');
    Route::post('/loans', [\App\Http\Controllers\MemberLoanController::class, 'store'])->name('loans.store');
    Route::get('/loans/{id}', [\App\Http\Controllers\MemberInstallmentController::class, 'show'])->name('loans.show');
    Route::post('/installments/{id}/pay', [\App\Http\Controllers\MemberInstallmentController::class, 'pay'])->name('installments.pay');
    
    // Savings routes
    Route::get('/savings', [\App\Http\Controllers\MemberSavingsController::class, 'index'])->name('savings.index');
    Route::post('/savings', [\App\Http\Controllers\MemberSavingsController::class, 'store'])->name('savings.store');
    
    // Profile routes generated by Breeze
    Route::get('/profile', [ProfileController::class, 'edit'])->name('profile.edit');
    Route::patch('/profile', [ProfileController::class, 'update'])->name('profile.update');
    Route::delete('/profile', [ProfileController::class, 'destroy'])->name('profile.destroy');
});

// Admin Routes
